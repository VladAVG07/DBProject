CREATE TABLE PROIECT_BD.SOFER
(
    SOFER_ID      NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NUME          VARCHAR2(50) NOT NULL,
    PRENUME       VARCHAR2(50) NOT NULL,
    DATA_NASTERE  DATE         NOT NULL,
    DATA_ANGAJARE DATE         NOT NULL,
    SALARIU       NUMBER(8, 2) NOT NULL
);

CREATE TABLE PROIECT_BD.VEHICUL
(
    VEHICUL_ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    MARCA               VARCHAR2(50) NOT NULL,
    MODEL               VARCHAR2(50) NOT NULL,
    NR_KILOMETRI        INTEGER      NOT NULL,
    AN_FABRICATIE       INTEGER      NOT NULL,
    CAPACITATE_REZERVOR INTEGER      NOT NULL
);

CREATE TABLE PROIECT_BD.AUTOCAR
(
    VEHICUL_ID     NUMBER PRIMARY KEY,
    NR_LOCURI      INTEGER       NOT NULL,
    SPARTIU_BAGAJE NUMBER(5, 2)  NOT NULL,
    DOTARI         VARCHAR2(200) NOT NULL,
    FOREIGN KEY (VEHICUL_ID) REFERENCES PROIECT_BD.VEHICUL (VEHICUL_ID) ON DELETE CASCADE
);

CREATE TABLE PROIECT_BD.TIR
(
    VEHICUL_ID            NUMBER PRIMARY KEY,
    CAPACITATE_CILINDRICA INTEGER      NOT NULL,
    GREUTATE_MAXIMA       NUMBER(5, 2) NOT NULL,
    TIP_TRACTIUNE         VARCHAR2(4)  NOT NULL,
    NR_AXE                INTEGER      NOT NULL,
    FOREIGN KEY (VEHICUL_ID) REFERENCES PROIECT_BD.VEHICUL (VEHICUL_ID) ON DELETE CASCADE
);

CREATE TABLE PROIECT_BD.CLIENT
(
    CLIENT_ID  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NUME       VARCHAR2(50)  NOT NULL,
    DESCRIERE  VARCHAR2(100) NOT NULL,
    TIP_CLIENT VARCHAR2(50)  NOT NULL,
    NR_TELEFON VARCHAR(10)   NOT NULL,
    EMAIL      VARCHAR2(100) NOT NULL UNIQUE,
    JUDET      VARCHAR2(50)  NOT NULL,
    ORAS       VARCHAR2(50)  NOT NULL
);

CREATE TABLE PROIECT_BD.MENTENANTA
(
    MENTENANTA_ID  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    VEHICUL_ID     NUMBER        NOT NULL,
    TIP_MENTENANTA VARCHAR2(50)  NOT NULL,
    DESCRIERE      VARCHAR2(100) NOT NULL,
    COST           NUMBER(14, 4) NOT NULL,
    DATA_ORA_START TIMESTAMP     NOT NULL,
    DATA_ORA_END   TIMESTAMP     NOT NULL,
    FOREIGN KEY (VEHICUL_ID) REFERENCES PROIECT_BD.VEHICUL (VEHICUL_ID) ON DELETE CASCADE
);

CREATE TABLE PROIECT_BD.ACCIDENT
(
    ACCIDENT_ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    VEHICUL_ID  NUMBER        NOT NULL,
    DESCRIERE   VARCHAR2(100) NOT NULL,
    COST_DAUNE  NUMBER(14, 4) NOT NULL,
    DATA_ORA    TIMESTAMP     NOT NULL,
    LOC         VARCHAR2(50)  NOT NULL,
    GRAVITATE   VARCHAR2(20)  NOT NULL,
    AMIABIL     BOOLEAN       NOT NULL,
    FOREIGN KEY (VEHICUL_ID) REFERENCES PROIECT_BD.VEHICUL (VEHICUL_ID) ON DELETE CASCADE
);

CREATE TABLE PROIECT_BD.ALIMENTARE
(
    ALIMENTARE_ID        NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    VEHICUL_ID           NUMBER        NOT NULL,
    LOC_ALIMENTARE       VARCHAR2(50)  NOT NULL,
    TIP_COMBUSTIBIL      VARCHAR2(20)  NOT NULL,
    CANTIATE_COMBUSTIBIL NUMBER(6, 2)  NOT NULL,
    DATA_ORA_ALIMENTARE  TIMESTAMP     NOT NULL,
    COST_DAUNE           NUMBER(14, 4) NOT NULL,
    BENZINARIE           VARCHAR2(20)  NOT NULL,
    FOREIGN KEY (VEHICUL_ID) REFERENCES PROIECT_BD.VEHICUL (VEHICUL_ID) ON DELETE CASCADE
);

CREATE TABLE PROIECT_BD.CURSA
(
    CURSA_ID         NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    VEHICUL_ID       NUMBER,
    LOC_PLECARE      VARCHAR2(100) NOT NULL,
    LOC_SOSIRE       VARCHAR2(100) NOT NULL,
    DATA_ORA_PLECARE TIMESTAMP     NOT NULL,
    DATA_ORA_SOSIRE  TIMESTAMP,
    COST_TOTAL       NUMBER(14, 4) NOT NULL,
    FOREIGN KEY (VEHICUL_ID) REFERENCES PROIECT_BD.VEHICUL (VEHICUL_ID) ON DELETE SET NULL
);


CREATE TABLE PROIECT_BD.TRANSPORT_MARFA
(
    CURSA_ID          NUMBER PRIMARY KEY,
    TIP_MARFA         VARCHAR2(50)  NOT NULL,
    GREUTATE_MARFA    NUMBER(7, 2)  NOT NULL,
    VOLUM_MARFA       NUMBER(7, 2)  NOT NULL,
    CONDITII_SPECIALE VARCHAR2(100),
    ASIGURARE_MARFA   NUMBER(14, 4) NOT NULL,
    FOREIGN KEY (CURSA_ID) REFERENCES PROIECT_BD.CURSA (CURSA_ID) ON DELETE CASCADE
);


CREATE TABLE PROIECT_BD.TRANSPORT_PERSOANE
(
    CURSA_ID      NUMBER PRIMARY KEY,
    NR_PASAGERI   INTEGER NOT NULL,
    GHID_TURISTIC BOOLEAN NOT NULL,
    FOREIGN KEY (CURSA_ID) REFERENCES PROIECT_BD.CURSA (CURSA_ID) ON DELETE CASCADE
);


CREATE TABLE PROIECT_BD.SOFER_VEHICUL
(
    SOFER_VEHICUL_ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    VEHICUL_ID       NUMBER,
    SOFER_ID         NUMBER,
    DATA_ORA_START   TIMESTAMP NOT NULL,
    DATA_ORA_END     TIMESTAMP,
    FOREIGN KEY (VEHICUL_ID) REFERENCES PROIECT_BD.VEHICUL (VEHICUL_ID) ON DELETE SET NULL,
    FOREIGN KEY (SOFER_ID) REFERENCES PROIECT_BD.SOFER (SOFER_ID) ON DELETE SET NULL
);

CREATE TABLE PROIECT_BD.CLIENT_CURSA
(
    CLIENT_ID NUMBER,
    CURSA_ID  NUMBER,
    FOREIGN KEY (CLIENT_ID) REFERENCES PROIECT_BD.CLIENT (CLIENT_ID) ON DELETE CASCADE,
    FOREIGN KEY (CURSA_ID) REFERENCES PROIECT_BD.CURSA (CURSA_ID) ON DELETE CASCADE,
    CONSTRAINT PK_CLIENT_CURSA PRIMARY KEY (CLIENT_ID, CURSA_ID)
);

